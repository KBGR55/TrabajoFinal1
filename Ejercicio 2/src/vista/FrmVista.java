    private void btnEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnviarActionPerformed
        if (txtNombre.getText().trim().isEmpty() || txtApellidos.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Campos vacios", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            if (ca.ingresoDatos(cbxOficinas.getSelectedIndex(), txtNombre.getText(), txtApellidos.getText())) {
                JOptionPane.showMessageDialog(null, "Se ha generado el ticket correctamente", "OK", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(null, "No hay ticket disponiples", "Error", JOptionPane.ERROR_MESSAGE);
            }
            txtOficinas.setText(ca.visualizar());
        }
    }//GEN-LAST:event_btnEnviarActionPerformed

